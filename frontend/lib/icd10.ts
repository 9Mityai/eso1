export function searchICD10(query: string) {
  const results: Array<{ code: string; title: string; description: string }> = [];
  const lowerQuery = query.toLowerCase();

  for (const category of Object.values(ICD10_REFERENCE)) {
    if (category.title.toLowerCase().includes(lowerQuery)) {
      results.push({
        code: "",
        title: category.title,
        description: category.title,
      });
    }
    for (const [code, description] of Object.entries(category.codes)) {
      if (
        code.toLowerCase().includes(lowerQuery) ||
        (typeof description === "string" && description.toLowerCase().includes(lowerQuery))
      ) {
        results.push({
          code,
          title: description as string,
          description: `${code} - ${description}`,
        });
      }
    }
  }

  return results.slice(0, 10);
}